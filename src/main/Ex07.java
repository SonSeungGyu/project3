package main;

public class Ex07 {
	public static void main(String[] args) {
		
		int x = 10; //오른쪽의값을 왼쪽변수에 대입한다.
		
		x += 10;
		System.out.println(x);

		x -= 5;
		System.out.println(x);
		
		x *= 10;
		System.out.println(x);
		
		x /= 10;
		System.out.println(x);
		
		x %= 10;
		System.out.println(x);
		
		
	}
}
